        console.log("balance 0 :",balance.toString());
        balance = await rToken.balanceOf(accounts[3]);
        console.log("balance 3 :",balance.toString());
        totalSupply = await rToken.totalSupply();
        console.log("totalSupply : ",totalSupply.toString());
